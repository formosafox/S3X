// Blink：建構
Blink::Blink() {
}
// ************************************************************************
// Blink：解構
Blink::~Blink(void){/*nothing to destruct*/}
// ************************************************************************
void Blink::Update(void) {
  switch (_Mode) {
    case Blink_ON   :
      _Mode = Blink_OFF;
      break;
    case Blink_OFF  :
      _Mode = Blink_Idle;
      break;
  }
}
